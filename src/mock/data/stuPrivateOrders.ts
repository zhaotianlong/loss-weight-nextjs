/**
 * 兼容旧接口的辅助函数，用于获取学员的所有私教订单及关联的上课记录
 */
export const getStuPrivateRecordsByStuId = (stuId: number) => {
  const orders = stuPrivateOrders.filter(o => o.stuId === stuId);
  return orders.map(order => ({
    ...order,
    classRecords: stuClassRecords.filter(r => r.orderId === order.orderId)
  }));
};
